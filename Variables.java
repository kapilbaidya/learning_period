class Variables{
    public static void main (String [] args){
      //String variables
      String name = "Kapil";
      System.out.println(name);
      
      //int variables
      int age = 22;
      System.out.println(age);
      
      //float variables
      float money = 100.02f;
      System.out.println(money);

      //char variables
      char section = 'A';
      System.out.println(section);

      //boolean variables
      boolean student = true;
      System.out.println(student);
        }
  }
