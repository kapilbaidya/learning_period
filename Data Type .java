class DataType{
  public static void main (String[]args) {
  
    byte a = 100;
    System.out.println(a);

    short b = 5000;
    System.out.println(b);

    int c = 100000;
    System.out.println(c);

    long d = 100000l;
    System.out.println(d);

    float x = 9999.99f;
    System.out.println(x);

    double y = 100000000000.01d;
    System.out.println(y);
  }
}
